- (NSString *)firstCharactor:(NSString *)aString
{
    NSMutableArray *charArr = [NSMutableArray array];
    for (int i = 0; i < aString.length; i ++) {
        NSString *s = [aString substringWithRange:NSMakeRange(i, 1)];
        //转成了可变字符串
        NSMutableString *str = [NSMutableString stringWithString:s];
        //先转换为带声调的拼音
        CFStringTransform((CFMutableStringRef)str,NULL, kCFStringTransformMandarinLatin,NO);
        //再转换为不带声调的拼音
        CFStringTransform((CFMutableStringRef)str,NULL, kCFStringTransformStripDiacritics,NO);
        //转化为大写拼音
        NSString *pinYin = [str capitalizedString];
        //获取并返回首字母
        [charArr addObject:[pinYin substringToIndex:1]];
    }
    
    return [charArr componentsJoinedByString:@""];
}

